#include "TIRGameInstance.h"

UTIRGameInstance::UTIRGameInstance(const FObjectInitializer& ObjectInitializer)
	: Super(ObjectInitializer)
{
	EnemyCountSpawn = 0;
	TimeMinute = 0;
}

/* Установить кол-во врагов */
void UTIRGameInstance::SetEnemyCountSpawn(int32 CountSpawn)
{
	EnemyCountSpawn = abs(CountSpawn);
}

/* Установить время игры */
void UTIRGameInstance::SetTimeGame(int32 NewMinute)
{
	TimeMinute = abs(NewMinute);
}



